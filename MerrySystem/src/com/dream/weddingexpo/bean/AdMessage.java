package com.dream.weddingexpo.bean;

public class AdMessage {
	
	private String adMessagePath;
	
	private String adMessageTitle;
	
	private String adMessageUrl;

	public String getAdMessagePath() {
		return adMessagePath;
	}

	public void setAdMessagePath(String adMessagePath) {
		this.adMessagePath = adMessagePath;
	}

	public String getAdMessageTitle() {
		return adMessageTitle;
	}

	public void setAdMessageTitle(String adMessageTitle) {
		this.adMessageTitle = adMessageTitle;
	}

	public String getAdMessageUrl() {
		return adMessageUrl;
	}

	public void setAdMessageUrl(String adMessageUrl) {
		this.adMessageUrl = adMessageUrl;
	}
	
}
